            if (filter.classList.contains('exchange__container__filter--close')) {
                filter.classList.remove('exchange__container__filter--close');
                filter.classList.add('exchange__container__filter--open');
            } else {
                filter.classList.add('exchange__container__filter--close');
                filter.classList.remove('exchange__container__filter--open');
            }
        });
