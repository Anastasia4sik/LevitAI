    //dropdown
        const dropdowns = document.querySelectorAll('.dropdown');

        dropdowns.forEach((dropdown) => {
            const dropdownTitle = dropdown.querySelector('.exchange__container__filter__dropdown__top')
            const dropdownContent = dropdown.querySelector('.dropdown-content');
            const dropdownContentTop = dropdown.querySelector('.exchange__container__filter__dropdown__top');

            dropdownTitle.addEventListener('click', () => {
                if(dropdownContent.classList.contains('open')) {
                    dropdownContent.classList.remove('open');

                    dropdownContentTop.style.paddingBottom = '0px';
                } else {
                    dropdownContent.classList.add('open');

                    dropdownContentTop.style.paddingBottom = '15px';
                }
            })
        })

    //range
        const rangeInput = document.querySelectorAll(".range-input input"),
        priceInput = document.querySelectorAll(".price-input input"),
        range = document.querySelector(".slider .progress");
        let priceGap = 1000;

        priceInput.forEach(input =>{
            input.addEventListener("input", e =>{
                let minPrice = parseInt(priceInput[0].value),
                maxPrice = parseInt(priceInput[1].value);

                if((maxPrice - minPrice >= priceGap) && maxPrice <= rangeInput[1].max){
                    if(e.target.class === "input-min"){
                        rangeInput[0].value = minPrice;
                        range.style.left = ((minPrice / rangeInput[0].max) * 100) + "%";
                    } else {
                        rangeInput[1].value = maxPrice;
                        range.style.right = 100 - (maxPrice / rangeInput[1].max) * 100 + "%";
                    }
                }
            });
        });

        rangeInput.forEach(input =>{
            input.addEventListener("input", e =>{
                let minVal = parseInt(rangeInput[0].value),
                maxVal = parseInt(rangeInput[1].value);
                if ((maxVal - minVal) < priceGap) {
                    if(e.target.class === "range-min"){
                        rangeInput[0].value = maxVal - priceGap
                    } else {
                        rangeInput[1].value = minVal + priceGap;
                    }
                } else {
                    priceInput[0].value = minVal;
                    priceInput[1].value = maxVal;
                    range.style.left = ((minVal / rangeInput[0].max) * 100) + "%";
                    range.style.right = 100 - (maxVal / rangeInput[1].max) * 100 + "%";
                }
            });
        });

    //disable
        document.addEventListener('DOMContentLoaded', function() {
            const mainBlock = document.querySelector('.exchange__container__table');
            const disableBlock = mainBlock.querySelector('.exchange__container__table__catalog__disable');

            if (mainBlock.classList.contains('disable')) {
                disableBlock.style.display = 'block';
            } else {
                disableBlock.style.display = 'none';
            }
        });

    //open filters
        const openFilterBtn = document.querySelector('.exchange__container__filter__btn');
        const filter = document.querySelector('.exchange__container__filter');

